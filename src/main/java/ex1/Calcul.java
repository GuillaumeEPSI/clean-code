package ex1;

public class Calcul {

    /**
     * Calcul la somme de deux entiers
     *
     * @param a Le premier entier à additionner
     * @param b Le deuxième entier à additionner
     * @return La somme des deux paramètres
     */
    public static int addition(int a, int b) {
        return a + b;
    }

    /**
     * Calcul la différence entre deux entiers
     *
     * @param a Le premier entier
     * @param b Le deuxième entier à soustraire
     * @return La différence entre les deux paramètres
     */
    public static int soustraction(int a, int b) {
        return a - b;
    }
}
